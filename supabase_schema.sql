-- 1. Create categories table
CREATE TABLE IF NOT EXISTS categories (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    description text,
    image_url text
);

-- 2. Create services table
CREATE TABLE IF NOT EXISTS services (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    description text,
    image_url text,
    category_id bigint REFERENCES categories(id) ON DELETE CASCADE,
    base_price numeric NOT NULL DEFAULT 1500,
    amc_applicable boolean NOT NULL DEFAULT false
);

-- 3. Create projects table
CREATE TABLE IF NOT EXISTS projects (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    client_name text NOT NULL,
    description text,
    completion_date date,
    image_url text,
    category_id bigint REFERENCES categories(id) ON DELETE CASCADE
);

-- 4. Create gallery table
CREATE TABLE IF NOT EXISTS gallery (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title text NOT NULL,
    image_url text,
    category_id bigint REFERENCES categories(id) ON DELETE CASCADE
);

-- 5. Create testimonials table
CREATE TABLE IF NOT EXISTS testimonials (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name text NOT NULL,
    position text,
    company text,
    content text NOT NULL,
    rating integer NOT NULL DEFAULT 5,
    image_url text
);

-- 6. Create website_settings table
CREATE TABLE IF NOT EXISTS website_settings (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_name text NOT NULL,
    email text,
    phone text,
    address text,
    whatsapp_number text,
    about_text text,
    logo_url text
);

-- 7. Create bookings table
CREATE TABLE IF NOT EXISTS bookings (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    full_name text NOT NULL,
    phone text NOT NULL,
    email text NOT NULL,
    address text NOT NULL,
    preferred_date date NOT NULL,
    time_slot text NOT NULL,
    message text,
    status text NOT NULL DEFAULT 'PENDING',
    category_id bigint REFERENCES categories(id) ON DELETE SET NULL,
    service_id bigint REFERENCES services(id) ON DELETE SET NULL
);

-- 8. Create contact_messages table
CREATE TABLE IF NOT EXISTS contact_messages (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
    full_name text NOT NULL,
    email text NOT NULL,
    phone text,
    subject text NOT NULL,
    message text NOT NULL,
    status text NOT NULL DEFAULT 'UNREAD'
);

-- 9. Create admins table
CREATE TABLE IF NOT EXISTS admins (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username text NOT NULL UNIQUE,
    password text NOT NULL,
    email text,
    full_name text
);

